        isTrue = 5+2 !=10;
        System.out.println("5+2 !=7 Result: "+isTrue);

        //4) ">" -> "Greater than" sign in Java

        isTrue = 12 > 13;
        System.out.println("12 > 13 Result is: " +isTrue);

        //5"<" -> "Less than" sign  in java
        isTrue = 12 < 13;
        System.out.println("12 < 13 Result is: " + isTrue);

        //6) ">=" —> "Greater than or equal to" sign in
        isTrue = 12 >= 12;
